def _filler_entries() -> list[Entry]:
    reserved = {e.date for e in _HANDCRAFTED}
    out: list[Entry] = []
    i = 0
    for start, end, step in (
        (dt.date(2025, 1, 2), dt.date(2025, 12, 31), 6),
        (dt.date(2026, 1, 5), dt.date(2026, 6, 30), 10),
    ):
        day = start
        while day <= end:
            iso = day.isoformat()
            if iso not in reserved:
                out.append(Entry(iso, _WEEKDAYS[day.weekday()], _filler_text(i)))
                i += 1
            day += dt.timedelta(days=step)
    return out


