def build_zip(path: Path) -> Path:
    """Write the mock diary as a Standard Notes backup ZIP at `path`."""
    items: list[dict] = []
    tag_refs: dict[str, list[str]] = {}
    for e in ENTRIES:
        note_uuid = _uuid(f"{e.date}:{e.title}")
        items.append(
            {
                "uuid": note_uuid,
                "content_type": "Note",
                "created_at": f"{e.date}T20:30:00.000Z",
                "updated_at": f"{e.date}T21:00:00.000Z",
                "content": {
                    "title": f"{e.date} {e.title}",
                    "text": e.text,
                    "references": [],
                },
            }
        )
        for tag in e.tags:
            tag_refs.setdefault(tag, []).append(note_uuid)
    items.extend(EDGE_ITEMS)

    backup = {"version": SN_VERSION, "items": items}
    with zipfile.ZipFile(path, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(
            "Standard Notes Backup and Import File.txt",
            json.dumps(backup, ensure_ascii=False, indent=2),
        )
        for tag, refs in sorted(tag_refs.items()):
            tag_file = {
                "uuid": _uuid(f"tag:{tag}"),
                "content_type": "Tag",
                "title": tag,
                "references": [{"uuid": u, "content_type": "Note"} for u in refs],
            }
            zf.writestr(
                f"Items/Tag/{tag}.txt",
                json.dumps(tag_file, ensure_ascii=False, indent=2),
            )
    return path


